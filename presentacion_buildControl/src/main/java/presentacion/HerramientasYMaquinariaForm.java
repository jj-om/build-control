    class ButtonRenderer extends JButton implements javax.swing.table.TableCellRenderer {

        public ButtonRenderer() {
            setOpaque(true); // que no sea transparente
        }

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            setText((value == null) ? "" : value.toString()); // asignarle el texto del bot√≥n
            return this;
        }
    }
    
    class ButtonEditor extends DefaultCellEditor {

        private JButton button;
        private boolean isIncrement;
        private int row;

        public ButtonEditor(JCheckBox checkBox, boolean isIncrement) {
            super(checkBox);
            this.isIncrement = isIncrement;
            button = new JButton();
            button.setOpaque(true);
            
            // el action listener del bot√≥n
            button.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    int cantidad = (int) tableModel.getValueAt(row, 2);
                    if (isIncrement) {
                        tableModel.setValueAt(cantidad + 1, row, 2); // incrementar
                    } else {
                        if (cantidad > 1) {
                            tableModel.setValueAt(cantidad - 1, row, 2); // decrementar
                        } else {
                            tableModel.removeRow(row); // eliminiarlo si llega a 0
                        }
                    }
                }
            });
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            this.row = row; 
            button.setText((value == null) ? "" : value.toString()); // asignarle el texto del bot√≥n
            return button;
        }
    }
    
